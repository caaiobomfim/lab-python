import math

print("Math library examples in Python:\n")

# Constant values
print("π (pi):", math.pi)
print("e (Euler's number):", math.e)

# Square root
print("√25 =", math.sqrt(25))

# Power
print("2³ =", math.pow(2, 3))

# Absolute value
print("|-10| =", math.fabs(-10))

# Factorial
print("5! =", math.factorial(5))

# Floor and ceil
print("math.floor(7.8) =", math.floor(7.8))
print("math.ceil(7.2) =", math.ceil(7.2))

# Trigonometric functions
angle = math.radians(90)
print("sin(90°):", math.sin(angle))
print("cos(90°):", math.cos(angle))
print("tan(90°):", math.tan(angle))

# Logarithms
print("log10(1000):", math.log10(1000))
print("ln(e):", math.log(math.e))

# Greatest common divisor (GCD)
print("gcd(24, 36):", math.gcd(24, 36))
